void scale_sprite(Sprite *sprite, OBJ_AFFINE *obj_aff_buffer_location, int obj_affine_num, int scale_factor)
{
	// the building sprites need to be affine so that we can scale them
	sprite->mem_addr->attr0 |= ATTR0_AFF | ATTR0_AFF_DBL;
	sprite->mem_addr->attr1 |= ATTR1_AFF_ID(obj_affine_num);
	OBJ_AFFINE *sprite_aff = obj_aff_buffer_location;
	obj_aff_identity(sprite_aff);
	FIXED scale = (1<<8)/scale_factor;
	obj_aff_scale(sprite_aff, scale, scale);
}

void set_player_attributes(OBJ_ATTR *obj_buffer, Sprite** players)
{
	
	// screen resolution is 240x160
	// player sprite is 32x32
	// sprite location is given by the top left index
	// thus player is center if at
	// x = (240/2 - 1) - (32/2 - 1) = 102
	// y = (160/2 - 1) - (32/2 - 1) = 64
	// define player at center of screen if at (102, 64)
	// fill sprite slots 1 through APPLE_MAX with apple sprites

	for(int i = 0; i < PLAYER_MAX; i++)
	{
		Sprite *newPlayer = mkSpriteStruct(&obj_buffer[0], PLAYER_SIZE, PLAYER_FRAME1, PLAYER_PAL_BANK, 1, 102, 64);
		newPlayer->throwing = 0; // set that the player is not throwing
		players[i] = newPlayer;
	}

}

void set_apples_attributes(OBJ_ATTR *obj_buffer, Sprite** apples)
{
	
	// fill sprite slots 1 through APPLE_MAX with apple sprites
	for(int i = 0; i < APPLE_MAX; i++){

		int sprite_num = 1 + i;
		int x = 2 + (i*8); // space them apart by 8 pixels
		int y = 24;

		// create new apple sprite
		Sprite *newApple = mkSpriteStruct(&obj_buffer[sprite_num], APPLE_SIZE, APPLE_FRAME1, APPLE_PAL_BANK, 1, x, y);
		newApple->index = i; // set the index of the apple
		newApple->active = 0; // set as not active (has not been thrown yet)
		apples[i] = newApple; // add to list of apples
	}

}

void set_squirrels_attributes(OBJ_ATTR *obj_buffer, Sprite** squirrels)
{
	// fill sprite slots (1+APPLE_MAX) through SQUIRREL_MAX with squirrel sprites
	for(int i = 0; i < SQUIRREL_MAX; i++){

		int sprite_num = 1 + APPLE_MAX + i;
		// set squirrels in the corner for now since they are hidden anyway
		int x = 0;
		int y = 0;

		// create new squirrel sprite
		Sprite *newSquirrel = mkSpriteStruct(&obj_buffer[sprite_num], SQUIRREL_SIZE, SQUIRREL_FRAME1, SQUIRREL_PAL_BANK, 0, x, y);
		newSquirrel->index = i; // set the index of the squirrel
		newSquirrel->dir_facing = -1; // the sprites were drawn facing the wrong direction. May fix later.
		newSquirrel->active = 0; // set as not active (has been hit)
		squirrels[i] = newSquirrel; // add to list of squirrels
		obj_hide(newSquirrel->mem_addr); // hide the sprite
	}
}

void set_buildings_attributes(OBJ_ATTR *obj_buffer, OBJ_AFFINE *obj_aff_buffer, Sprite** buildings)
{
	
	// array of building locations
	int building_positions[8][2] = {
		{-183, -71}, 	// detchon
		{-70, -128}, 	// hays
		{69, -97}, 		// center
		{205, -127},	// baxter
		{-51, 134}, 	// lilly
		{83, 127}, 		// sparks
		{188, 96}, 		// goodrich
		{248, 3} 		// chapel
		};

	// array of building hit box sizes {hbx, hby}
	int building_hit_boxes[8][2] = {
		{38, 32}, 		// detchon
		{64, 50}, 		// hays
		{65, 26}, 		// center
		{58, 46}, 		// baxter
		{37, 63}, 		// lilly
		{38, 38}, 		// sparks
		{37, 22}, 		// goodrich
		{41, 22} 		// chapel
		};
	
	int building_num = 0; // track index in position array
	
	// fill remaining slots with building sprites
	for(int i = 0; i < BUILDINGS_MAX; i++){
		int sprite_num = 1 + APPLE_MAX + SQUIRREL_MAX + i;

		int x = building_positions[building_num][0];
		int y = building_positions[building_num][1];
		int hbx = building_hit_boxes[building_num][0];
		int hby = building_hit_boxes[building_num][1];

		// if second half of the sprite
		if( (i%2) != 0){
			y+=64; // move the sprite below top half
			building_num++; // increment building number
		}


		Sprite *newBuilding = mkSpriteStruct(&obj_buffer[sprite_num], BUILDINGS_SIZE, BUILDING_FRAME1 + (i*32), BUILDINGS_PAL_BANK, 1, x, y);
		newBuilding->index = i; // set the index of the building
		scale_sprite(newBuilding, &obj_aff_buffer[i], i, 2); // scale by a factor of 2

		// determine if building should be hidden
		int bottom = newBuilding->y_pos + 64;
		int right = newBuilding->x_pos + 128;
		int top = newBuilding->y_pos;

		if( (top > SCREEN_HEIGHT) || (bottom < 0) || ( right < 0) ){
			newBuilding->hidden = 1; // mark as hidden
			obj_hide(newBuilding->mem_addr); // hide the building sprites
		}
		newBuilding->hbx = hbx;
		newBuilding->hby = hby;
		buildings[i] = newBuilding; // add to list of buildings

		
		
	}
	obj_aff_copy(obj_aff_mem, obj_aff_buffer, BUILDINGS_MAX);
}